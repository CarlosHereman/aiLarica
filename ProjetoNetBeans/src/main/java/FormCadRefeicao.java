    public void excluir(){
        refeicao = new Refeicao();
      
         try{
            refeicao.setIdProd(cxId.getText());
            
            refeicao = gerRefeicao.consProdId(refeicao);
         
            if(refeicao != null){
                
                cxId.setText(Integer.toString(refeicao.getIdProd()));
                cxNome.setText(refeicao.getNomeProd());
                
                int x = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir o refeicao?", "Exclusão de refeicao", JOptionPane.YES_NO_CANCEL_OPTION);
                if(x == 0){
                    gerRefeicao.removeProdId(refeicao);
                    JOptionPane.showMessageDialog(null, "Refeicao removida com sucesso!", "Remoção de refeicao", 1);
                    limpar();
                }
            }
            else{
              JOptionPane.showMessageDialog(null, "Não existe refeicao com este ID!", "Refeicao inexistente", 0);
              cxId.setText("");
              cxId.requestFocus();
            }
        }
        catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(null, "O ID deve ser um inteiro!", "ERRO de ID", 0);
            cxId.setText("");
            cxId.requestFocus();
        } //fim catch

    
    }
    
