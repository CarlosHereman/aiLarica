    public void listarTab(){
        DefaultTableModel modelo = (DefaultTableModel) tblProd.getModel();
        
        int posLin = 0;
        modelo.setRowCount(posLin);
        
        for(Refeicao refeicao : gerRefeicao.getBdRef()){
            modelo.insertRow(posLin, new Object[]{refeicao.getIdProd(), refeicao.getNomeProd(), refeicao.getPreco(), refeicao.getFornecedor(), refeicao.getQuant(), refeicao.getDesc(), refeicao.getTempo(), refeicao.getIngred(), refeicao.getAcomp()});
            posLin++;
        }
    }
    
