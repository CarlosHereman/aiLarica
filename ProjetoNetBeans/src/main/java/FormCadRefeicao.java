    public void cadastrar(){
        refeicao = new Refeicao();
        try{
            refeicao.setIdProd(cxId.getText());
            refeicao.setNomeProd(cxNome.getText());
            refeicao.setPreco(cxPreco.getText());
            refeicao.setQuant(cxQuant.getText());
            refeicao.setDesc(cxDesc.getText());
            refeicao.setFornecedor(cxForn.getText()); //Não há como verificar se Fornecedor já foi cadastrado
            refeicao.setTempo(cxTempo.getText());
            refeicao.setIngred(cxIngred.getText());
            refeicao.setAcomp(cxAcomp.getText());
                 
         refeicao = gerRefeicao.cadProd(refeicao);
         
         if(refeicao != null){
            refeicao.impDados();
            limpar();
         }
         else{
            JOptionPane.showMessageDialog(null, "Já existe uma refeicao com este codigo!", "ERRO de ID", 0);
            cxId.setText("");
            cxId.requestFocus();
         }
        } 
        catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(null, "O ID deve ser um inteiro!", "ERRO de ID", 0);
            cxId.setText("");
            cxId.requestFocus();
        }
        catch (NumberException ne) {
            StackTraceElement[] stackTrace = ne.getStackTrace();
            if (stackTrace[0].getMethodName().equals("setQuant")) {
                JOptionPane.showMessageDialog(null, "A quantidade deve ser um inteiro!", "ERRO de Quantidade", 0);
                cxQuant.setText("");
                cxQuant.requestFocus();
            } else if (stackTrace[0].getMethodName().equals("setTempo")) {
                JOptionPane.showMessageDialog(null, "O tempo deve ser um inteiro!", "ERRO de inserção", 0);
                cxTempo.setText("");
                cxTempo.requestFocus();
            } else {
                JOptionPane.showMessageDialog(null, "Erro desconhecido!", "ERRO", 0);
            }        
        } 
        catch (DecimalNumberException dne) {
            JOptionPane.showMessageDialog(null, "O preço deve ser um valor valido!", "ERRO de Preço", 0);
            cxPreco.setText("");
            cxPreco.requestFocus();
        }
        
    }
    
